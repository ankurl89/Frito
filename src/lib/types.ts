export interface BrandDNA {
  id: string;
  user_id: string;

  // Lightweight identity (generated in conversational onboarding)
  name: string;
  tagline: string;
  story: string;
  niche: string;
  target_audience: string;
  brand_values: string[];
  voice_tone: string;
  price_tier: "budget" | "mid" | "premium" | "luxury";
  palette: ColorPalette;
  typography: Typography;
  logo_url?: string;
  logo_prompt?: string;

  // Full Brand Book — generated in background after launch.
  // brand_book_status: "pending" | "generating" | "ready" | "failed"
  brand_book?: BrandBook;
  brand_book_status?: "pending" | "generating" | "ready" | "failed";

  // Public storefront identity
  slug?: string;
  storefront_config?: Record<string, unknown>;

  status: "draft" | "active";
  created_at: string;
  updated_at: string;
}

/**
 * Complete Brand Book — the source of truth for all future brand decisions.
 * Generated by the AI Brand Strategist after initial onboarding.
 *
 * Every AI-generated asset (product listings, designs, emails, social posts)
 * should reference this for consistency.
 */
export interface BrandBook {
  // ── Foundation ────────────────────────────────────────────
  alternative_names: string[];
  domain_suggestions: string[];
  brand_meaning: string;             // why the name works + emotional resonance

  // ── Strategy ──────────────────────────────────────────────
  mission: string;
  vision: string;
  brand_promise: string;

  // ── Customer Persona ─────────────────────────────────────
  persona: {
    age_range: string;
    gender_mix: string;
    location: string;
    income: string;
    interests: string[];
    buying_behavior: string;
    motivations: string;
    pain_points: string;
    aspirations: string;
  };

  // ── Personality & Archetype ───────────────────────────────
  personality_traits: { trait: string; how_it_shows: string }[];
  archetype: string;                 // Creator | Explorer | Hero | Rebel | Sage | Everyman | …
  archetype_reasoning: string;

  // ── Tone of Voice ─────────────────────────────────────────
  we_are: string[];                  // ["Confident", "Conversational"…]
  we_are_not: string[];              // ["Corporate", "Pushy"…]

  // ── Messaging ─────────────────────────────────────────────
  taglines: string[];
  headlines: string[];
  copy_examples: { instead_of: string; say: string }[];

  // ── Visual identity (detail) ──────────────────────────────
  palette_meanings: { color: string; hex: string; meaning: string }[];
  typography_detail: {
    headline_font: string;
    body_font: string;
    fallback_fonts: string;
    usage_rules: string;
  };
  visual_style: {
    photography: string;
    illustration: string;
    design_direction: string;
    mood: string;
  };

  // ── Brand guidelines ──────────────────────────────────────
  logo_usage_do: string[];
  logo_usage_dont: string[];
  approved_color_combos: string[];
  typography_hierarchy: string;
  social_visual_direction: string;

  // ── Product positioning ───────────────────────────────────
  core_categories: string[];
  hero_products: string[];
  future_expansion: string[];
  launch_collection: string[];

  // ── Pricing strategy ──────────────────────────────────────
  pricing_strategy: {
    entry_range: string;
    core_range: string;
    premium_range: string;
    reasoning: string;
  };

  // ── Competitive positioning ───────────────────────────────
  competitors: string[];
  why_choose_us: string;
  positioning_statement: string;

  // ── Packaging ─────────────────────────────────────────────
  packaging: {
    style: string;
    unboxing_experience: string;
    brand_inserts: string;
    thank_you_message: string;
    sticker_concepts: string;
  };

  // ── Social media playbook ─────────────────────────────────
  social: {
    instagram_bio: string;
    tiktok_bio: string;
    content_pillars: string[];
    posting_themes: string[];
    hashtags: string[];
  };

  // ── Email marketing foundation ────────────────────────────
  email_tones: {
    welcome: string;
    abandoned_cart: string;
    launch: string;
    support: string;
  };
}

