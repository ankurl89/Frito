/**
 * Product = the Digital Twin of a sellable item.
 * Every artifact (artwork, mockup, production file, lifestyle) derives from this.
 */
export interface Product {
  id: string;
  brand_id: string;
  name: string;
  description: string;
  category: string;

  // ── Supplier template binding ─────────────────────────────
  qikink_product_id: string;       // → ProductTemplate.id in catalog
  qikink_product_name: string;
  template_view: "front" | "back" | "side";

  // ── Pricing ───────────────────────────────────────────────
  base_cost: number;
  sell_price: number;

  // ── Digital twin assets ───────────────────────────────────
  design_prompt: string;            // creative prompt for AI artwork
  artwork_url?: string;             // standalone graphic (transparent bg)
  design_url?: string;              // legacy alias of artwork_url
  mockup_url?: string;              // template + artwork composited (catalog image)
  production_file_url?: string;     // print-ready PNG at print DPI
  placement?: {
    /** One of the 4 named placements (V1 catalog rule) — the starting box. */
    key: "front_center" | "back_center" | "front_pocket" | "full_back";
    /** Fine-tune within/around the placement box. Defaults: 1 / 0 / 0. */
    scale?: number;
    offset_x?: number;
    offset_y?: number;
  };
  /** Garment colors offered for this product (buyer picks one). */
  colors?: ("Black" | "White" | "Navy" | "Maroon")[];

  sku: string;
  variants: ProductVariant[];

  // ── Listing ───────────────────────────────────────────────
  listing_title: string;
  listing_description: string;
  seo_tags: string[];

  // ── New lifecycle / SEO fields ────────────────────────────
  discount_price?: number | null;
  url_slug?: string;
  meta_title?: string;
  meta_description?: string;
  tags?: string[];
  version?: number;
  published_at?: string | null;
  archived_at?: string | null;

  // PVE — visualization quality
  visualization_score?: number | null;
  validation_report?: Record<string, unknown>;

  // Lifecycle states per PRD. "active" kept for backward compat.
  status: "draft" | "published" | "archived" | "active";
  created_at: string;
}

/** Snapshot taken every time a product changes materially. */
export interface ProductVersion {
  id: string;
  product_id: string;
  version: number;
  snapshot: Partial<Product>;
  change_type: "create" | "edit" | "artwork" | "pricing" | "publish" | "unpublish" | "archive";
  change_summary?: string;
  created_at: string;
}

